/bin 	用户常用的一些命令放在/bin目录下
/boot 	有关启动的一些中药文件放在/boot目录下
/dev 	系统中设备的设备文件保存在/dev目录下
/etc 	系统中的各种配置文件
/home	系统中普通用户的家目录
/lib 	系统运行所需的各种库文件
/lib32	如果运行的是64位系统，还需要一个/lib32的目录，用于保存兼容32位程序的库文件
/media	默认的自动挂载光盘、U盘等存储介质的目录
/mnt	可以在mnt目录下创建一些子目录作为其他分区的挂载点
/opt 	有一些第三方商业的应用程序会安装到/opt目录下
/proc	系统运行时所有进程的信息，是一个虚拟的文件系统
/root	超级用户家目录
/sbin	存放一下系统管理命令
/srv	保存系统所提供服务的一些数据文件，如web服务器的网页文件
/sys	保存整个系统中所有硬件设备的信息
/tmp	用于保存一些临时文件
/usr	用于安装软件程序
/var	包含一些变化的数据文件

