## 删除
	x 删除单个字符
	dw 从光标处删除至下一个单词起始处
	d$ 从当前光标删除到行末
	de 从当前光标删除到单词末尾，包括最后一个字符

## 文本编辑之插入
	i 插入

## 文本编辑之添加
	A 追加

## 使用计数指定动作
	2w 光标向前移动2个单词
	2e 光标向前移动到第2个单词的末尾
	0  移动光标到行首
	