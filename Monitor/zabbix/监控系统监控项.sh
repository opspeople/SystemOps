主机监控
	CPU、内存、磁盘的剩余空间/利用率和I/O、SWAP使用率、系统UP时间、进程数、负载

网卡监控
	Ping的往返时间及包成功率、网卡流量，包括流入/流出量和错误的数据包数

文件监控
	监控文件大小、Hash值，匹配查询、字符串存在与否

URL监控
	监测指定URL访问过程中的返回码、下载时间及文件大小，支持内容匹配

应用程序
	端口和内存使用率、CPU使用率、服务状态、请求数、并发连接数、消息队列的字节数、Client事务处理数、Services状态数

数据库
	监测数据库中指定的表空间、数据库的游标数、Session数、事务数、死锁数、缓冲池命中率、库Cache命中率、当前连接数、进程的内存利用率等性能参数

日志
	错误日志匹配、特定字符串匹配

硬件
	温度、风扇转速、电压等

