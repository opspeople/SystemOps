	每个类型的权限： 读、写、执行

	附加标记
		suid(Set User ID),sgid(Set Group ID)及sticky(定义文件模式)
		suid 进程执行一个文件时通常保持进程拥有者的UID
		sgid 进程执行一个文件时保持进程组的GID
		sticky 设置了sticky标志位的可执行文件相应地对内核发出一个请求，当它执行结束以后，依然将该程序保留在内存中

