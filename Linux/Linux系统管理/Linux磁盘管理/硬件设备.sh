硬件设备在Linux中的文件名
	SCSI、SATA、USB接口的磁盘： /dev/sd[a-d]
	U盘： /dev/sd[a-p]
	Virtio: /dev/vd[a-p]
	打印机： /dev/lp[0-7] USB接口的打印机： /dev/usb/lp[0-7]
	软盘： /dev/fd[0-7]
	鼠标-通用接口： /dev/input/mouse[0-15]
	鼠标-PS/2接口： /dev/psaux
	鼠标-当前鼠标： /dev/mouse
	CD-ROM,DVD-ROM： /dev/cdrom /dev/scd[0-1] /dev/sr[0-1]
	IDE磁盘驱动器： /dev/hd[a-d] 
