操作系统的目标
	1.与硬件部分相互作用，为所有包含在硬件平台上的低层可编程部件提供服务
	2.为运行在计算机系统上的应用程序提供一个执行环境

多用户系统特点
	1.认证机制，核实用户身份
	2.保护机制，防止有错误的用户程序妨碍其他应用程序在系统中运行
	3.保护机制，防止有恶意的用户程序干涉或窥视其他用户的活动
	4.记账机制，限制分配给每个用户的资源数

硬链接和软连接
# 一个目录中的文件名就是一个文件的硬链接(hard link),简称链接。
# 在同一目录或不同的目录中，同一文件可以有几个链接，因此对应几个文件名。
	ln f1 f2   # 创建一个硬链接
	硬链接限制：
		不允许给目录创建硬链接
		只有在同一文件系统中的文件之间才能创建链接

	ln -s f1 f2	# 创建一个软连接

